<script>
export default {
  name: 'FullRequest',
  mounted() {
    // Get references to the tab links and content divs
    const tabLinks = this.$el.querySelectorAll('.data-head .link');
    const contentDivs = this.$el.querySelectorAll('.data-catcher > div');

    // Show the initial default content (ad-details) and hide the others
    contentDivs[0].style.display = 'block';
    // eslint-disable-next-line no-plusplus
    for (let i = 1; i < contentDivs.length; i++) {
      contentDivs[i].style.display = 'none';
    }
    tabLinks[0].classList.add('link-active');

    // Add click event listeners to the tab links
    tabLinks.forEach((tabLink, index) => {
      tabLink.addEventListener('click', () => {
        // Hide all content divs
        contentDivs.forEach((contentDiv) => {
          // eslint-disable-next-line no-param-reassign
          contentDiv.style.display = 'none';
        });

        // Show the clicked content div
        contentDivs[index].style.display = 'block';

        // Remove active class from all tab links
        tabLinks.forEach((link) => {
          link.classList.remove('link-active');
        });

        // Add active class to the clicked tab link
        tabLink.classList.add('link-active');
      });
    });
  },
};
</script>
